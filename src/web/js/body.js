exports.postMessage_socketio = (postMessage_data_socketio) => {
  // add pathname to request
  postMessage_data_socketio = Object.assign(postMessage_data_socketio, { path: window.location.pathname });
  if (this.worker_socketio) {
    // worker give object event containing { data: postMessage_data_socketio, .. }
    this.worker_socketio.postMessage(postMessage_data_socketio);
  } else { // use callback
    // giving object containing data like the event
    postMessage_data_socketio = { data: postMessage_data_socketio };
    require('./body/socketio')._onmessage(postMessage_data_socketio, (onmessage_cb_data) => {
        onmessage_common_socketio(onmessage_cb_data);
    });
  }
}

